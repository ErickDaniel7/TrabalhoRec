    private void jSalvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSalvarButtonActionPerformed
        preparaLabel();
        if (validarCampos() == true){
            if (livroSelecionado.getId()!= null){
                livroSelecionado.setTitulo(jtituloField.getText());
                livroSelecionado.setAutor(jautorField.getText());
                livroSelecionado.setEditora(jeditoraField.getText());
                livroSelecionado.setAno(Integer.valueOf(janoField.getText().trim()));
                livroSelecionado.setExemplaresDisponiveis(Integer.valueOf(jquantidadeField.getText().trim()));
                service.atualizar(livroSelecionado);
                preencherTabela(null);
                
            }else {
                livroSelecionado = new Livro();
                livroSelecionado.setTitulo(jtituloField.getText());
                livroSelecionado.setAutor(jautorField.getText());
                livroSelecionado.setEditora(jeditoraField.getText());
                livroSelecionado.setAno(Integer.valueOf(janoField.getText().trim()));
                livroSelecionado.setExemplaresDisponiveis(Integer.valueOf(jquantidadeField.getText().trim()));
                service.salvar(livroSelecionado);
                preencherTabela(null);
            }
        }
    }//GEN-LAST:event_jSalvarButtonActionPerformed
