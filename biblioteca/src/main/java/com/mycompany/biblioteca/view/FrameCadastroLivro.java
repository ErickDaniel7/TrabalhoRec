    private void preencherTabela(List<Livro> livros) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Limpa a tabela
        if (livros == null) livros = service.listarLivro();
        for (Livro livro : livros) {
            model.addRow(new Object[]{
                livro.getId().toString(),
                livro.getTitulo(),
                livro.getAutor(),
                livro.getEditora(),
                livro.getAno().toString(),
                livro.getExemplaresDisponiveis().toString()
            });
        }
    }

    /*Pegar id a partir da celula da tabela e chamar serviço para obter cliente especifico */
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow != -1) {
            // Obtém os valores das células da linha selecionada com verificação de null
            String id = getCellValueAsString(jTable1, selectedRow, 0);
            selecionarLivro(id);
        }
    }

     private String getCellValueAsString(JTable table, int row, int column) {
        Object value = table.getValueAt(row, column);
        return value != null ? value.toString() : "";
    }
     
    private void selecionarLivro(String id){
        if (id !=null){
            livroSelecionado  = service.obterLivroPorId(Integer.valueOf(id));
            if (livroSelecionado!=null){
                jtituloField.setText(livroSelecionado.getTitulo());
                jautorField.setText(livroSelecionado.getAutor());
                jeditoraField.setText(livroSelecionado.getEditora());
                janoField.setText(livroSelecionado.getAno().toString());
                jquantidadeField.setText(livroSelecionado.getExemplaresDisponiveis().toString());
                jSalvarButton.setText("Atualizar");
                jRemoverButton.setEnabled(true);
            }
        }
    } 
